#include "gateitem.h"
#include "arrow.h"

#include <QGraphicsScene>
#include <QGraphicsSceneContextMenuEvent>
#include <QMenu>
#include <QPainter>

//! [0]
GateItem::GateItem(GateType gateType, QMenu *contextMenu,
             QGraphicsItem *parent)
    : QGraphicsPixmapItem(parent)
{
    myGateType = gateType;
    myContextMenu = contextMenu;
    switch (myGateType) {
        case AND: myPixmap.load(":/images/gates/and.png"); break;
        case NAND: myPixmap.load(":/images/gates/nand.png");break;
        case OR:myPixmap.load(":/images/gates/or.png");break;
        case NOR:myPixmap.load(":/images/gates/nor.png");break;
        case NOT:myPixmap.load(":/images/gates/not.png");break;
        case XNOR:myPixmap.load(":/images/gates/xnor.png");break;
        case XOR:myPixmap.load(":/images/gates/xor.png");break;
    }
    setPixmap(myPixmap);
    setFlag(QGraphicsItem::ItemIsMovable, true);
    setFlag(QGraphicsItem::ItemIsSelectable, true);
    setFlag(QGraphicsItem::ItemSendsGeometryChanges, true);
}
//! [0]

//! [1]
void GateItem::removeArrow(Arrow *arrow)
{
    int index = arrows.indexOf(arrow);

    if (index != -1)
        arrows.removeAt(index);
}
//! [1]

//! [2]
void GateItem::removeArrows()
{
    foreach (Arrow *arrow, arrows) {
        arrow->startItem()->removeArrow(arrow);
        arrow->endItem()->removeArrow(arrow);
        scene()->removeItem(arrow);
        delete arrow;
    }
}
//! [2]

//! [3]
void GateItem::addArrow(Arrow *arrow)
{
    arrows.append(arrow);
}
//! [3]

//! [4]

//! [5]
void GateItem::contextMenuEvent(QGraphicsSceneContextMenuEvent *event)
{
    scene()->clearSelection();
    setSelected(true);
    myContextMenu->exec(event->screenPos());
}
//! [5]

//! [6]
QVariant GateItem::itemChange(GraphicsItemChange change, const QVariant &value)
{
    if (change == QGraphicsItem::ItemPositionChange) {
        foreach (Arrow *arrow, arrows) {
            arrow->updatePosition();
        }
    }

    return value;
}
